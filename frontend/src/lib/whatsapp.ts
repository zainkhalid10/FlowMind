/**
 * WhatsApp click-to-chat helpers.
 *
 * We avoid a server-side WhatsApp Business API bot (needs Meta approval,
 * paid plan, verified phone number) and instead use the universal
 * `https://wa.me/?text=...` deep link. On desktop it opens WhatsApp Web;
 * on mobile it opens the native app. The user picks the recipient, so
 * this works for both:
 *   - Marketing share  (recipient = anyone the user wants to tell)
 *   - Send summary     (recipient = the client the manager picks)
 *
 * No API keys. No bot server. Instant.
 */

const MAX_URL_SAFE_MESSAGE = 4000;

export function buildWhatsappShareUrl(opts: {
  /** The text the app pre-fills in WhatsApp. */
  message: string;
  /** Optional phone with country code (digits only, no + or spaces). */
  phone?: string;
}): string {
  const phone = (opts.phone || "").replace(/[^\d]/g, "");
  const text = encodeURIComponent(opts.message.slice(0, MAX_URL_SAFE_MESSAGE));
  return phone
    ? `https://wa.me/${phone}?text=${text}`
    : `https://wa.me/?text=${text}`;
}

/** Opens WhatsApp in a new tab. Blocked popups fall back to same-tab navigation. */
export function openWhatsapp(url: string): void {
  const win = window.open(url, "_blank", "noopener,noreferrer");
  if (!win) window.location.assign(url);
}

