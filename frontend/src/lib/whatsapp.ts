export function documentSummaryMessage(opts: {
  filename: string;
  totalRequirements: number;
  functional?: number;
  nonFunctional?: number;
  business?: number;
  system?: number;
  reviewUrl?: string;
  managerName?: string;
}): string {
  const {
    filename,
    totalRequirements,
    functional = 0,
    nonFunctional = 0,
    business = 0,
    system = 0,
    reviewUrl,
    managerName,
  } = opts;
  const parts: string[] = [];
  parts.push(`📄 *FlowMind — Requirement summary*`);
  if (managerName) parts.push(`_from ${managerName}_`);
  parts.push("");
  parts.push(`*Document:* ${filename}`);
  parts.push(`*Total requirements extracted:* ${totalRequirements}`);
  parts.push("");
  parts.push(`• Functional: ${functional}`);
  parts.push(`• Non-functional: ${nonFunctional}`);
  parts.push(`• Business: ${business}`);
  parts.push(`• System: ${system}`);
  if (reviewUrl) {
    parts.push("");
    parts.push(`👉 Open the full review:\n${reviewUrl}`);
  }
  parts.push("");
  parts.push(`_Generated by FlowMind · AI-powered requirements engineering_`);
  return parts.join("\n");
}

/** The manager's-eye summary, used when copying-to-Slack or similar. */
export function inviteShareMessage(opts: {
  clientName: string;
  email: string;
  tempPassword: string;
  inviteLink: string | null;
  filename?: string;
}): string {
  const lines: string[] = [];
  lines.push(`🔐 *FlowMind review invitation*`);
  lines.push("");
  lines.push(`Hi ${opts.clientName}, you've been invited to review requirements`);
  if (opts.filename) lines.push(`from *${opts.filename}*.`);
  lines.push("");
  lines.push(`*Sign-in email:* ${opts.email}`);
  lines.push(`*Temporary password:* \`${opts.tempPassword}\``);
  if (opts.inviteLink) {
    lines.push("");
    lines.push(`*One-click invite link:*\n${opts.inviteLink}`);
  }
  lines.push("");
  lines.push(`_The password is single-use — change it after signing in._`);
  return lines.join("\n");
}
